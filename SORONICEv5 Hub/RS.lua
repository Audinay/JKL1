local function YTSOB_fake_script() -- sirius.Button_3.LocalScript 
	local script = Instance.new('LocalScript', sirius.Button_3)

	--// SERVICES
	local Players = game:GetService("Players")
	local player = Players.LocalPlayer
	
	--// VARIABLES
	local AutoClick = false
	local AutoClickThread
	local ToolName = "Weight" -- ‚ö†Ô∏è nom EXACT du tool
	
	local button = script.Parent -- ton bouton existant
	
	--// AUTCLICK FUNCTION
	local function StartAutoClick()
		if AutoClickThread then return end
	
		AutoClickThread = task.spawn(function()
			while AutoClick do
				local character = player.Character
				if not character then
					task.wait()
					continue
				end
	
				-- Cherche le tool √©quip√©
				local tool = character:FindFirstChild(ToolName)
	
				-- Sinon, le cherche dans le Backpack et l'√©quipe
				if not tool then
					tool = player.Backpack:FindFirstChild(ToolName)
					if tool then
						tool.Parent = character
					end
				end
	
				-- Active le tool
				if tool then
					tool:Activate()
				end
	
				-- Cooldown SAFE (ne peut pas casser le script)
				local cooldown = 0.0000000000000001
				if tool then
					local attr = tool:GetAttribute("SwingCooldown")
					if typeof(attr) == "number" then
						cooldown = attr
					end
				end
	
				task.wait(cooldown)
			end
	
			AutoClickThread = nil
		end)
	end
	
	--// BOUTON CLICK (ON / OFF)
	button.MouseButton1Click:Connect(function()
		AutoClick = not AutoClick
	
		if AutoClick then
			StartAutoClick()
		end
	end)
	
	--// RESET AU RESPAWN
	player.CharacterAdded:Connect(function()
		AutoClick = false
	end)
	
end
